import { readdirSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const ROOT = 'public/Flashbacks'
const WEB = /\.(jpe?g|png)$/i
const natural = (a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' })

const years = readdirSync(ROOT, { withFileTypes: true })
  .filter(d => d.isDirectory())
  .map(d => d.name)
  .sort(natural)

const body = years.map(year => {
  const files = readdirSync(join(ROOT, year))
    .filter(f => WEB.test(f))
    .sort(natural)
    .map(f => `    ${JSON.stringify(`/Flashbacks/${year}/${f}`)},`)
    .join('\n')
  return `  ${year}: [\n${files}\n  ],`
}).join('\n')

writeFileSync('src/data/galleries.ts',
`// AUTO-GENERATED by scripts/build-galleries.mjs — do not edit by hand.
// Regenerate after adding/removing photos: node scripts/build-galleries.mjs
export const GALLERIES: Record<number, string[]> = {
${body}
}
`)
console.log('Wrote src/data/galleries.ts')
